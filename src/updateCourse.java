    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here: 
        try{
           Class.forName("com.mysql.jdbc.Driver");
	Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cms", "root", "root");
        PreparedStatement p = con.prepareStatement("update course set cname=?, duration=?, fees=? where cid=?;");
        String cidd = cid.getText();
        String cnm = cname.getText();
        String cdur = dur.getText();
        String cfee = fee.getText();
        int cfeee = Integer.parseInt(cfee);
        p.setString(4, cidd);
        p.setString(1, cnm);
        p.setString(2, cdur);
        p.setInt(3, cfeee);
        int i=p.executeUpdate();
        if(i==0) {
            JOptionPane.showMessageDialog(null, "Record not updated");
        }
        else{
            JOptionPane.showMessageDialog(null, "Record updated");
        }
        con.close();
        
        cid.setText("");
        cname.setText("");
        dur.setText("");
        fee.setText("");
        dur.setEditable(false);
        fee.setEditable(false);
        }
        catch(HeadlessException | ClassNotFoundException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

