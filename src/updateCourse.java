    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
           Class.forName("com.mysql.jdbc.Driver");
	Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cms", "root", "root");
	PreparedStatement ps = con.prepareStatement("select * from course");
        ResultSet rs = ps.executeQuery();
        String cidd = cid.getText();
        String cnm = cname.getText();
        while(rs.next()) {
            if(cidd.equals(rs.getString(1)) && cnm.equals(rs.getString(2))) {
                dur.setText(rs.getString(3));
                fee.setText(rs.getString(4));
                dur.setEditable(true);
                fee.setEditable(true);
            }
        }
        String cdur = dur.getText();
        if(cdur.equals("")) {
            JOptionPane.showMessageDialog(null, "Record not found");
            cid.setText("");
            cname.setText("");
        }
        
        con.close();
        }
        catch(HeadlessException | ClassNotFoundException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

